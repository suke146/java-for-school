public class CalcExample2 {
    public static void main(String args[]) {
        int num = 0;
        int num2 = 0;
        int i = 10;
        int j = i * 2;

        System.out.println(num++);
        num++;
        System.out.println(num);

        System.out.println(++num2);
        System.out.println(num2);

        System.out.println(i);
        System.out.println(j);
    }
}
